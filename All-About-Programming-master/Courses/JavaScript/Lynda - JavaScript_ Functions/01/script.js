// 1. We declared a function with the keywords function
// 2. We can give it a name like there the name is plus
// 3. We can give parameters here we have "a" and "b"
// 4. Inside the "{}" we can add the statements for the function
// 5. Use the "return" statements
function plus(a, b) {
    var sum = a + b;
    return sum; // In javascript we can return anything
}

plus(2, 2); // Invoking the function

console.log(plus(2, 2));

// Result: 4
